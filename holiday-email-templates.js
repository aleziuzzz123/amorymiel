// Function to get holiday template by key
export function getHolidayTemplate(holidayKey) {
  return holidayTemplates[holidayKey] || holidayTemplates["new-year"];
}

// Function to get all holiday templates
export function getAllHolidayTemplates() {
  return holidayTemplates;
}

// Function to get holidays by theme
export function getHolidaysByTheme(theme) {
  return Object.entries(holidayTemplates)
    .filter(([key, template]) => template.theme === theme)
    .map(([key, template]) => ({ key, ...template }));
}
